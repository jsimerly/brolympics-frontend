/**
 * Generate TESTING.md: every vitest test as a plain-English claim, grouped by
 * file and describe block. The describe/it names ARE the catalog -- write them
 * as full sentences. Regenerate with:  npm run test:catalog
 */
import { execSync } from 'node:child_process'
import { readFileSync, unlinkSync, writeFileSync } from 'node:fs'

const REPORT = '.vitest-report.json'

try {
  execSync(`npx vitest run --reporter=json --outputFile=${REPORT}`, {
    stdio: 'ignore',
  })
} catch {
  // vitest exits non-zero on failures; the report still gets written and a
  // catalog of a red suite is still worth reading
}

const report = JSON.parse(readFileSync(REPORT, 'utf8'))
unlinkSync(REPORT)

const lines = [
  '# Test catalog',
  '',
  'Generated by `npm run test:catalog` -- regenerate after adding tests,',
  "don't edit by hand. Each line is the behavior the test pins, from its",
  'describe/it names (write them as sentences).',
  '',
  'Run the suite: `npm test` (check the EXIT CODE -- never pipe to tail).',
]

let total = 0
const files = [...report.testResults].sort((a, b) =>
  a.name.localeCompare(b.name)
)
for (const file of files) {
  const rel = file.name
    .replaceAll('\\', '/')
    .replace(/^.*?\/frontend\//, '')
  lines.push('', `## ${rel} (${file.assertionResults.length} tests)`, '')
  let lastGroup = null
  for (const test of file.assertionResults) {
    const group = test.ancestorTitles.filter(Boolean).join(' › ')
    if (group !== lastGroup) {
      lastGroup = group
      lines.push(`- **${group}**`)
    }
    const mark = test.status === 'passed' ? '' : `  [${test.status.toUpperCase()}]`
    lines.push(`  - ${test.title}${mark}`)
    total += 1
  }
}
lines.push('', '---', '', `${total} tests cataloged.`, '')

writeFileSync('TESTING.md', lines.join('\n'))
console.log(`wrote TESTING.md (${total} tests)`)
